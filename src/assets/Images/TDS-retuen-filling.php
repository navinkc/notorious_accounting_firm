                    <p style="margin-bottom: 20px;">TDS is the Tax deducted at source at the time of making a payment to a person and is mandatory to be paid as per the Income Tax Act 1961. When an individual deducts these taxes has to deposit TDS with the government. As per the rules,
                        the employer who deducts the tax is known as the deductor and before making the payment he deducts the tax and renders it to the customer who is referred to as the deductee. This tax is then deposited with the central government
                        of India.<br></p>
